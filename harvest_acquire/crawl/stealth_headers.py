def random_ua() -> str:
    """Return a randomly selected user-agent string."""
    return random.choice(USER_AGENTS)


def stealth_headers(user_agent: Optional[str] = None) -> Dict[str, str]:
    """
    Return a dict of realistic browser request headers.
    Includes Accept, Accept-Language, Sec-Ch-Ua, Sec-Fetch-* headers.
    """
    ua = user_agent or random_ua()
    is_chrome = "Chrome" in ua and "Firefox" not in ua and "Safari" in ua
    headers: Dict[str, str] = {
        "User-Agent": ua,
        "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,image/avif,image/webp,*/*;q=0.8",
        "Accept-Language": "en-US,en;q=0.9",
        "Accept-Encoding": "gzip, deflate, br",
        "Connection": "keep-alive",
        "Upgrade-Insecure-Requests": "1",
    }
    if is_chrome:
        # Extract major version for Sec-Ch-Ua
        import re
        m = re.search(r"Chrome/(\d+)", ua)
        major = m.group(1) if m else "124"
        headers.update({
            "Sec-Ch-Ua": f'"Chromium";v="{major}", "Google Chrome";v="{major}", "Not-A.Brand";v="99"',
            "Sec-Ch-Ua-Mobile": "?0",
            "Sec-Ch-Ua-Platform": '"Windows"',
            "Sec-Fetch-Dest": "document",
            "Sec-Fetch-Mode": "navigate",
            "Sec-Fetch-Site": "none",
            "Sec-Fetch-User": "?1",
        })
    return headers


def respect_retry_after(response_headers: Dict[str, str]) -> None:
    """
    Block the current thread for the duration requested by Retry-After header.
    Cap at 60 seconds to avoid infinite waits.
    """
    retry_after = response_headers.get("Retry-After") or response_headers.get("retry-after")
    if not retry_after:
        return
    try:
        delay = min(float(retry_after), 60.0)
        time.sleep(delay)
    except ValueError:
        pass
